//==================================================
// 定義
//==================================================
namespace
{
}

//==================================================
// 静的メンバ変数
//==================================================
float CDomino::m_move = 0.0f;
int CDomino::m_count = 0;

//--------------------------------------------------
// 生成
//--------------------------------------------------
int CDomino::GetCount()
{
	return m_count;
}

//--------------------------------------------------
// 生成
//--------------------------------------------------
void CDomino::SetCount(int score)
{
	m_count = score;
}

//--------------------------------------------------
// 生成
//--------------------------------------------------
void CDomino::AddCount()
{
	m_count++;
}

//--------------------------------------------------
// 生成
//--------------------------------------------------
void CDomino::AddMove()
{
	m_move += 0.1f;
}

//--------------------------------------------------
// 生成
//--------------------------------------------------
void CDomino::CreateAll()
{
	if (m_count == 0)
	{

	}

	m_move = 0.0f;
	D3DXVECTOR3 pos = D3DXVECTOR3(0.0f, 0.0f, 0.0f);

	for (int i = 0; i < m_count; i++)
	{
		// 生成
		CDomino::Create(pos);

		pos.z += 20.0f;
	}
}

//--------------------------------------------------
// 生成
//--------------------------------------------------
CDomino* CDomino::Create(const D3DXVECTOR3& pos)
{
	CDomino* pDomino = new CDomino;

	if (pDomino == nullptr)
	{// nullチェック
		assert(false);
		return nullptr;
	}

	// 初期化
	pDomino->Init();

	// 位置の設定
	pDomino->SetPos(pos);

	return pDomino;
}

//--------------------------------------------------
// デフォルトコンストラクタ
//--------------------------------------------------
CDomino::CDomino()
{
}

//--------------------------------------------------
// デストラクタ
//--------------------------------------------------
CDomino::~CDomino()
{
}

//--------------------------------------------------
// 初期化
//--------------------------------------------------
void CDomino::Init()
{
	// 初期化
	CModel::Init();

	// 使用するモデルの設定
	CModel::SetLabel(CFileXManager::LABEL_BAMBOO);
}

//--------------------------------------------------
// 終了
//--------------------------------------------------
void CDomino::Uninit()
{
	// 終了
	CModel::Uninit();
}

//--------------------------------------------------
// 更新
//--------------------------------------------------
void CDomino::Update()
{
	// 位置の取得
	D3DXVECTOR3 pos = CModel::GetPos();

	if (pos.z <= -300.0f)
	{// 範囲外に出た
		CObject::SetRelease();
	}

	// 更新
	CModel::Update();
}

//--------------------------------------------------
// 描画
//--------------------------------------------------
void CDomino::Draw()
{
	// 描画
	CModel::Draw();
}
